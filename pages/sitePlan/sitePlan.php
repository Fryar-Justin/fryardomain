    <h1>Site Plan</h1>
    <h2>Site Purpose Statement:</h2>

    <p>fryardomain.com is going to be a sight designed to help make some headway into the realm of quadcopter racing. This is a new field that has just cropped up and from what I can tell only in the last year or so. It is beginning to make news headlines and many people are beginning to search on the internet for it. There are beginning to have more and more venues open for this budding sport yet it can be difficult to find them or even know how to start. My website will include information about these events.</p>

    <h2>Target Audience:</h2>

    <p>My audience I believe will be between the ages of 12+ with higher concentrations in the 16-18 year range. Individuals that visit my site will, for the most part, not have a great knowledge of quadcopter equipment. I believe this will be a very interesting part of the site that will draw attention. This portion of the site doesn’t need to have just lists of good parts but an educational factor to it. If I could create possibly some videos that educate the viewer on this sport and quadcopters in general. This will be a fairly expensive sport so I expect visitors to have at least moderate access to money and so should have a decent education or success in the working environment. This site will be viewable on desktop computers and mobile phones. I would like to create an apple and android app for this but that will be outside of the scope of this course and timetable I have to make it. Later down the road perhaps.</p>

<h2>Male Persona:</h2>

<p>My name is Aaron. I live in Loveland, Colorado and I work as a photographer. I have always enjoyed photography and film making. I graduated with a BFA (Bachelors of Fine Arts) in Filmmaking. Since then I began doing side work for things like weddings, adventure tours, and real estate. The real estate bit of my business has really picked up recently. In this part of my business when a house goes up for sale it needs to be listed on the local MLS site. I take fantastic photographs of the house that are coupled with the listing and help it to sell better. I recently added 3D photography to my services and I am investigating Quadcopter photography. I would use this to take ariel photographs of the house and the surrounding neighborhood to also with with the listing. It will add a new and unique perspective that will intrigue realtors and viewers alike. Because my field of study is in photography and not operation of a quadcopter I don’t know much about the equipment being used, gyroscopes or anything else. I am starting from scratch at this point. I need to know equipment costs, operating costs, what happens if it crashes etc. I would also like to know the local laws and limitations of flying this “drone.” What are my liabilities? I’m also fascinated at the idea of taking these drones with me on hikes in the mountains and using the drones for photography up there. What are the implications of doing this?</p>

