<h2>Scenarios & Content List:</h2>
<ul>
<li>What are the legalities of quadcopter racing? Where can I fly it? Do I need to be licensed?</li>
</ul>
<p>All of these questions have everything to do with the current legislation and is constantly changing in an attempt to catch up to the technology. My site will contain a legalities section that will give the latest information on what is happening in that field. I would need to be careful to offset any accountability on my part having dispersing false or out of date information.</p>
<ul>
<li>How will I know where to go to see these races?</li>
</ul>
<p>My site will have a localities page designed at the beginning to point people towards events happening in the Colorado area. Eventually it will spread to other states.</p>
<ul>
<li>How do I enter a race?</li>
</ul>
<p>The site with local races will also contain a contact method for each of these events. This may be as simple as an email address and phone number but could also include links to the organizers website if available. In an attempt to simplify the site and keep regular maintenance low I do not plan to make a method of entering the race through my site itself.</p>
<ul>
<li>What equipment do I need to get started?</li>
</ul>
<p>This will be contained in the educational portion of the site. It will feature beginner videos that will explain the basics of quadcopter flight and how it works.</p>
<ul>
<li>What other things can I go with a drone?</li>
</ul>
<p>The site will contain ideas and possibly areas where you can legally fly a drone such as in the mountains or over water, with warnings of course. Many people don’t consider the possibilities of drones and you can give them ideas.</p>
<ul>
<li>What skill training is available?</li>
</ul>
<p>Many people don’t have the money to spend $300 or $400 every time they crash to fix their drone. There are simulators that can be purchased that use the actual controller of the quadcopter and it’s much cheaper to crash a simulation than it is the real thing. If there are also any trainers available in the area I would list those with some reviews from some other clients.</p>
<ul>
<li>Where can I fly my drone just for fun?</li>
</ul>
<p>I will include a list/map of areas that are drone friendly and fun to fly in</p>

